      national, socio-economic, cultural, linguistic, political and educational
      backgrounds - UWC recognises that the curriculum has to allow for as many
      interpretations and perceptions as there are students in the programme, so
      that the students feel able to represent their own history and perspective
      authentically. The formal IBDP curriculum is therefore complemented by
